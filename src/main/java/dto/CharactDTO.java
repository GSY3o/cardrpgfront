package dto;

public class CharactDTO {

	String name;
	String charClass;
	String force;
	String agi;
	String intel;
	String charisme;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCharClass() {
		return charClass;
	}

	public void setCharClass(String charClass) {
		this.charClass = charClass;
	}

	public String getForce() {
		return force;
	}

	public void setForce(String force) {
		this.force = force;
	}

	public String getAgi() {
		return agi;
	}

	public void setAgi(String agi) {
		this.agi = agi;
	}

	public String getIntel() {
		return intel;
	}

	public void setIntel(String intel) {
		this.intel = intel;
	}

	public String getCharisme() {
		return charisme;
	}

	public void setCharisme(String charisme) {
		this.charisme = charisme;
	}

}
